public class assignment6 {
    public static void main(String[] args) {
        Scanner reader = new Scanner(System.in);  // Reading from System.in
System.out.println("Enter a number: ");
int n = reader.nextInt(); 
 
 
        
for (int j = 0; j > n - 1; j--) {
           
                System.out.println(j-j);
                
            }
        
        
 for (int j = 0; j < n+1; j++) {
           
                System.out.println(j+j);
                
            }
        }
            }
    

